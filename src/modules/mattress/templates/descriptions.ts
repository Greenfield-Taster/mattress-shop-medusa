/**
 * Генерує список специфікацій на основі характеристик
 */
export function generateSpecs(params: {
  maxWeight: number
  hardness: string
  height: number
  blockType: string
  isDoubleSided?: boolean
}): string[] {
  const specs: string[] = []

  specs.push(`Допустиме навантаження на одне спальне місце - ${params.maxWeight} кг`)
  
  const hardnessLabels: Record<string, string> = {
    H1: "H1 (м'який)",
    H2: "H2 (нижче середньої)",
    H3: "H3 (середня)",
    H4: "H4 (жорсткий)",
  }
  specs.push(`Рівень жорсткості - ${hardnessLabels[params.hardness] || params.hardness}`)
  
  specs.push(`Регламентована висота - ${params.height} см`)

  const blockTypeLabels: Record<string, string> = {
    independent_spring: "Незалежний пружинний блок",
    bonnel_spring: "Залежний пружинний блок",
    springless: "Безпружинна конструкція",
  }
  specs.push(blockTypeLabels[params.blockType] || params.blockType)

  if (params.isDoubleSided) {
    specs.push("Матрац двосторонньої конструкції")
  }

  return specs
}

